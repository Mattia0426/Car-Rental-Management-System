    private void displayVehicleData() {
        try (
                // Establish a database connection
                Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/carrental?zeroDateTimeBehavior=CONVERT_TO_NULL", "root", ""); PreparedStatement preparedStatement = connection.prepareStatement("SELECT vid, model, year, mileage, cost, deposit, availability FROM vehicle;"); ResultSet resultSet = preparedStatement.executeQuery();) {
            DefaultTableModel tableModel = (DefaultTableModel) tblcar.getModel();

            // Clear existing data in the table
            tableModel.setRowCount(0);

            while (resultSet.next()) {
                String vid = resultSet.getString("vid");
                String model = resultSet.getString("model");
                String year = resultSet.getString("year");
                String mileage = resultSet.getString("mileage");
                String cost = resultSet.getString("cost");
                String deposit = resultSet.getString("deposit");
                String availability = resultSet.getString("availability");

                // Add the fetched data as a new row in the table
                tableModel.addRow(new Object[]{vid, model, year, mileage, cost, deposit, availability});
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
        }
        // TODO add your handling code here:
        // TODO add your handling code here:
    }


                        preparedStatement.setString(2, updatedyear);
                        preparedStatement.setString(3, updatedmile);
                        preparedStatement.setString(4, updatedcost);
                        preparedStatement.setString(5, updateddeposit);
                        preparedStatement.setString(6, updatedStatus);
                        preparedStatement.setString(7, updatedcid);
